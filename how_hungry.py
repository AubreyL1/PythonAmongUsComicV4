def draw_square(xpos, ypos, size, heading):
    #this function draws a square at a position at a angle starting at the bottom left if it is facing right
    pensize(10)
    penup()
    setposition(xpos, ypos)
    pendown()
    setheading(heading) #0 = right, 180 = left, 270 = down, 90 = up
    for i in range(4):
        forward(size)
        left(90)
    penup()
